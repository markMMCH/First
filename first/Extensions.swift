extension CALayer {
  func applySketchShadow(
    color: UIColor = #colorLiteral(red: 0.2745098174, green: 0.4862745106, blue: 0.1411764771, alpha: 0.8445794092),
    alpha: Float = 0.8,
    x: CGFloat = 0,
    y: CGFloat = 8,
    blur: CGFloat = 14,
    spread: CGFloat = 0)
  {
    shadowColor = color.cgColor
    shadowOpacity = alpha
    shadowOffset = CGSize(width: x, height: y)
    shadowRadius = blur / 2.0
    if spread == 0 {
      shadowPath = nil
    } else {
      let dx = -spread
      let rect = bounds.insetBy(dx: dx, dy: dx)
      shadowPath = UIBezierPath(rect: rect).cgPath
    }
  }
    
    func setupRadialGradient(centerColor: CGColor, cornerColor: CGColor) {
        let gradientLayer = CAGradientLayer()
        gradientLayer.type = .radial

        gradientLayer.colors = [centerColor, cornerColor]
        let center = CGPoint(x: 0.5, y: 0.5)
        gradientLayer.startPoint = center
        let radius = 1.5
        gradientLayer.endPoint = CGPoint(x: radius, y: radius)
    }
}

extension UIImage {
    static func from(color: UIColor) -> UIImage {
        let rect = CGRect(x: 0, y: 0, width: 1, height: 1)
        UIGraphicsBeginImageContext(rect.size)
        let context = UIGraphicsGetCurrentContext()
        context!.setFillColor(color.cgColor)
        context!.fill(rect)
        let img = UIGraphicsGetImageFromCurrentImageContext()
        UIGraphicsEndImageContext()
        return img!
    }
}



